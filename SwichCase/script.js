let dia = prompt('Digite um numero de 1 à 7');

switch(dia){
    case "1" : 
    alert("Domingo")
    console.log("Domingo")
    break
    case '2' :
    alert('Segunda')
    console.log("Segunda-feira")
    break
    case '3' :
        alert("Terça-feira")
    console.log("Terça-feira")
    break
    case '4' :
    alert("Quarta-feira")
    console.log("Quarta-feira")
    break
    case '5' :
    alert("Quinta-feira")
    console.log("Quinta-feira")
    break
    case '6' :
    alert("Sexta-feira")
    console.log("Sexta-feira")
    break
    case '7' :
    alert("Sabado")
    console.log("Sábado")
    break
    default :
    console.log("Dia inválido")
}